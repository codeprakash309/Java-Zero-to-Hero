//The String class valueOf() method  
//converts given type such as int,long,float,double,boolean,char and char array into String
class StringValueOFMethod 
{
	public static void main(String[] args) 
	{
		int n=10;
        char[] ch={'a','b','c','d'};
		float f=15.5f;
		boolean b=true;
		String s=String.valueOf(n);  
		System.out.println("int  to String  :  "+s+10);
		String str=String.valueOf(ch);
		System.out.println("char[] array to String : "+str);
		String str2=String.valueOf(f);
		System.out.println("float to string :  "+str2+10);
		String str3=String.valueOf(b);
		System.out.println("boolean to String : "+str3+false);
	}
}
