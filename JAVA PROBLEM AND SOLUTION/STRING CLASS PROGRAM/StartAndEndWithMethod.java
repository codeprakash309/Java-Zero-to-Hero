//the method startsWith() checks whether the string starts with the letter passed as an arguments
//and endsWith() method checks whether the string ends with the letter passed as an arguments
class StartAndEndWithMethod 
{
	public static void main(String[] args) 
	{
		String s="prakash chandra bagh";
		System.out.println(s.startsWith("pra"));
		System.out.println(s.endsWith("sh"));
		System.out.println(s.startsWith("chan"));
		System.out.println(s.endsWith("gh"));
	}
}
