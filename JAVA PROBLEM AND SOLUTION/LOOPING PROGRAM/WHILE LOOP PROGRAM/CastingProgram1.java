class CastingProgram1 
{
	public static void main(String[] args) 
	{
		byte b=122;
		short s=120;
		int n=129;
		long l=325;
		float f=5.5f;
		double d=15.8;
		char ch='b';
		System.out.println("int type to byte type casting  :"+(byte)n);
		System.out.println("byte type to int type casting  : "+(int)b);
		System.out.println("short type to int type casting : "+(int)s);
		System.out.println("int type to short type casting : "+(short)n);
		System.out.println("long type to byte type casting : "+(byte)l);
		System.out.println("float type to int type casting  : "+(int)f);
		System.out.println("int type to float type casting : "+(float)n);
		System.out.println("flaot type to double type casting : "+(double)f);
		System.out.println("int type to char type casting : "+(char)n);
		System.out.println("char type to int type casting : "+(int)ch);

		
	}
}
